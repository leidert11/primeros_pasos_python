# Multiplicación y división (* y /)
multi = 12 * 5  # Multiplica 12 por 5
division = 12 / 5  # Divide 12 entre 5, devuelve un dato float (flotante)

# Potenciación (exponente) (**)
exponente = 12 ** 5  # 12 elevado a la 5ta potencia

# División entera (//)
division_baja = 12 // 5  # Divide 12 entre 5 y devuelve el entero redondeado hacia abajo

# Resto o módulo (%)
resto = 12 % 5  # Calcula el resto de dividir 12 entre 5

# type(dato) devuelve el tipo de dato
